package pack06_bool;

public class Ex01_Bool {
	public static void main(String[] args) {
		//부울형(논리형) : boolean
		//true, false 만 기억 ▶ 조건식 작성 시 사용.
		//컴퓨터는 어떠한 판단을 true와 false로만 함.
		// 예시) 오늘 온도가 15도 보다 높습니까? 
		boolean t = true ;
		boolean f = false ;
		// 문자열도 표현하고싶고 변수도 표현하고싶다?
		System.out.println("변수 t에 들어있는값 : " + t);
		System.out.println("변수 f에 들어있는값 : " + f);
	}
}
